def analyze_jewelry_image(image_url):
    """Analyze uploaded jewelry image using Gemini Vision"""
    try:
        # Download image
        response = requests.get(image_url)
        if response.status_code != 200:
            return None
        
        # Use Gemini Vision to analyze
        vision_model = genai.GenerativeModel('gemini-pro-vision')
        
        prompt = """Analyze this jewelry image and identify:
1. Type of jewelry (earring, necklace, ring, bracelet, etc.)
2. Category (baby, women, men)
3. Style (traditional, modern, bridal, etc.)
4. Key features

Respond in this exact format:
Type: [jewelry type]
Category: [baby/women/men]
Style: [style]
Keywords: [comma separated keywords]"""
        
        image_parts = [
            {
                "mime_type": "image/jpeg",
                "data": response.content
            }
        ]
        
        ai_response = vision_model.generate_content([prompt, image_parts[0]])
        analysis = ai_response.text.strip()
        
        # Parse response
        lines = analysis.split('\n')
        result = {}
        for line in lines:
            if ':' in line:
                key, value = line.split(':', 1)
                result[key.strip().lower()] = value.strip().lower()
        
        return result
    
    except Exception as e:
        print(f"Image analysis error: {e}")
        return None

def find_similar_collection(image_analysis):
    """Find matching collection based on image analysis"""
    if not image_analysis:
        return None
    
    jewelry_type = image_analysis.get('type', '')
    category = image_analysis.get('category', '')
    keywords = image_analysis.get('keywords', '')
    
    # Combine for search
    search_text = f"{category} {jewelry_type} {keywords}"
    
    return smart_search_collection(search_text)

