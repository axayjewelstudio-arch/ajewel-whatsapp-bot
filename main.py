def send_catalog_link(to_number, collection_key, collection_name):
    """Open WhatsApp catalog by collection ID"""
    collection_id = CATALOG_COLLECTIONS.get(collection_key)
    
    if not collection_id:
        send_whatsapp_text(to_number, f"Sorry, {collection_name} catalog is currently unavailable. Please try again later.")
        return
    
    # WhatsApp catalog deep link
    catalog_url = f"https://wa.me/c/{WHATSAPP_PHONE_ID}/{collection_id}"
    
    message = f"✨ *{collection_name}*\n\nTap the button below to browse our collection in WhatsApp catalog."
    
    send_whatsapp_cta_button(
        to_number,
        message,
        f"View {collection_name}",
        catalog_url
    )

